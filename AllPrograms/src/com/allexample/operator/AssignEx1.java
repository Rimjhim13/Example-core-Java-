package com.allexample.operator;

public class AssignEx1 {
    public static void main(String[] args)
    {

        int a = 10;
        int b = 12;
        System.out.println("a+=b:"+(a+=b));
        System.out.println("a-=b:"+(a-=b));
        System.out.println("a*=b:"+(a*=b));
        System.out.println("a/=b:"+(a/=b));
        System.out.println("a%=b:"+(a%=b));



    }
}
